#ifndef __VALID_OBJECT_CHECK__
#define __VALID_OBJECT_CHECK__
// //////////////////////////////////////////////////////////// 
#pragma once
// //////////////////////////////////////////////////////////// 
template<class T>
bool IsValidObj(const T &pObj) { return pObj && pObj->IsValid() && pObj->IsAlive(); }

// //////////////////////////////////////////////////////////// 
#endif // __VALID_OBJECT_CHECK__