struct SMissionStatusObject
{
  DWORD params[4];// primary params: up to 4 different params
  DWORD armors[4];// armors
  DWORD weaponstats[4];// weapon stats
  int nScenarioIndex;// scenario unit index
  DWORD dwIconsStatus;// icons status: one bit for each icon, set for this object
  DWORD dwPlayer;// high word - diplomacy, low word - player, this unit belong to
  wchar_t pszName[128];// name of this object
  //
  int GetLow(int nIndex, const DWORD *vals) const { return static_cast<int>(short(vals[nIndex] & 0xffff)); }
  int GetHigh(int nIndex, const DWORD *vals) const { return static_cast<int>(short((vals[nIndex] >> 16) & 0xffff)); }
  float GetRelative(int nIndex, const DWORD *vals) const { return Clamp(GetHigh(nIndex, vals) == 0 ? 0.0f : static_cast<float>(GetLow(nIndex, vals)) / static_cast<float>(GetHigh(nIndex, vals)), 0.0f, 1.0f); }

  void GetSplit(int nIndex, const DWORD *vals, int *pnLow, int *pnHigh)
  {
    *pnLow = GetLow(nIndex, vals);
    *pnHigh = GetHigh(nIndex, vals);
  }

  //
  SMissionStatusObject() { Clear(); }

  void Clear()
  {
    Zero(*this);
    nScenarioIndex = -1;
  }
};

inline DWORD PackParams(int nLow, int nHigh) { return (static_cast<DWORD>(nHigh & 0xffff) << 16) | static_cast<DWORD>(nLow & 0xffff); }

